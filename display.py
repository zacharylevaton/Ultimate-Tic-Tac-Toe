def display_board(b):
    """
    A function to print the current board
    :param b: Takes in a dictionary containing the current board positions
    :return: Prints the current board
    """
    # A, B, C: 1-3
    print("\n ", b["A"][0], "|", b["A"][1], "|", b["A"][2], " || ", b["B"][0], "|", b["B"][1], "|", b["B"][2], " || ",
          b["C"][0], "|", b["C"][1], "|", b["C"][2])
    print(" ----------- || ----------- || -----------")

    # A, B, C: 4-6
    print(" ", b["A"][3], "|", b["A"][4], "|", b["A"][5], " || ", b["B"][3], "|", b["B"][4], "|", b["B"][5], " || ",
          b["C"][3], "|", b["C"][4], "|", b["C"][5])
    print(" ----------- || ----------- || -----------")

    # A, B, C: 7-9
    print(" ", b["A"][6], "|", b["A"][7], "|", b["A"][8], " || ", b["B"][6], "|", b["B"][7], "|", b["B"][8], " || ",
          b["C"][6], "|", b["C"][7], "|", b["C"][8])

    print("===========================================")

    # D, E, F: 1-3
    print(" ", b["D"][0], "|", b["D"][1], "|", b["D"][2], " || ", b["E"][0], "|", b["E"][1], "|", b["E"][2], " || ",
          b["F"][0], "|", b["F"][1], "|", b["F"][2])
    print(" ----------- || ----------- || -----------")

    # D, E, F: 4-6
    print(" ", b["D"][3], "|", b["D"][4], "|", b["D"][5], " || ", b["E"][3], "|", b["E"][4], "|", b["E"][5], " || ",
          b["F"][3], "|", b["F"][4], "|", b["F"][5])
    print(" ----------- || ----------- || -----------")

    # D, E, F: 7-9
    print(" ", b["D"][6], "|", b["D"][7], "|", b["D"][8], " || ", b["E"][6], "|", b["E"][7], "|", b["E"][8], " || ",
          b["F"][6], "|", b["F"][7], "|", b["F"][8])

    print("==========================================")

    # G, H, I: 1-3
    print(" ", b["G"][0], "|", b["G"][1], "|", b["G"][2], " || ", b["H"][0], "|", b["H"][1], "|", b["H"][2], " || ",
          b["I"][0], "|", b["I"][1], "|", b["I"][2])
    print(" ----------- || ----------- || -----------")

    # G, H, I: 4-6
    print(" ", b["G"][3], "|", b["G"][4], "|", b["G"][5], " || ", b["H"][3], "|", b["H"][4], "|", b["H"][5], " || ",
          b["I"][3], "|", b["I"][4], "|", b["I"][5])
    print(" ----------- || ----------- || -----------")

    # G, H, I: 7-9
    print(" ", b["G"][6], "|", b["G"][7], "|", b["G"][8], " || ", b["H"][6], "|", b["H"][7], "|", b["H"][8], " || ",
        b["I"][6], "|", b["I"][7], "|", b["I"][8])


